using System.IO;
using UnityEngine;

public static class SpriteHelper
{
    public static Sprite LoadNewSprite(string FilePath, float PixelsPerUnit = 100.0f, SpriteMeshType spriteType = SpriteMeshType.Tight)
    {
        Debug.Log(FilePath);
